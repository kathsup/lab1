package trabajoEnclaseSemana6;

public  class empleado {
   
  //atributos
    
    String nombre; 
    double salario; 
    char genero; 
    int edad; 
    int antiguedad; 

    public empleado(String nombre, double salario, char genero, int edad, int antiguedad) {
        this.nombre = nombre;
        this.salario = salario;
        this.genero = genero;
        this.edad = edad;
        this.antiguedad = antiguedad;
    }
    
    
    
    
}
